function convertToTensors(data, targets, testSplit) {
    const numExamples = data.length;
    if (numExamples !== targets.length) {
        throw new Error('data and split have different numbers of examples');
    }
    const numTestExamples = Math.round(numExamples * testSplit);
    const numTrainExamples = numExamples - numTestExamples;

    const xDims = data[0].length;

    //create a 2D 'tf.Tensor' to hold the feature data
    const xs = tf.tensor2d(data, [numExamples, xDims]);

    //Create a 1D  'tf.Tensor' to hold the labeles, and convert the number label
    //From the set (0,1,2) into one-hot encoding (.e.g.,0 --> [1,0,0]);

    const ys = tf.oneHot(tf.tensor1d(targets).toInt(), Ecommerce_NUM_PRODUCT);

    //Split the data into training and test sets, using "slice"
    const xTrain = xs.slice([0, 0], [numTrainExamples, xDims]);
    const xTest = xs.slice([numTrainExamples, 0], [numTestExamples, xDims]);
    const yTrain = ys.slice([0, 0], [numTrainExamples, Ecommerce_NUM_PRODUCT]);
    const yTest = ys.slice([0, 0], [numTestExamples, Ecommerce_NUM_PRODUCT]);
    return [xTrain, yTrain, xTest, yTest];
}

